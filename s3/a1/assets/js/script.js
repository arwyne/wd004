let day = "tuEsday"
day = day.toLowerCase()

switch(day) {
	case "monday":
		console.log("You need to wear Red");
		break;
	case "tuesday":
		console.log("You need to wear Orange");
		break;
	case "wednesday":
		console.log("You need to wear Yellow");
		break;
	case "thursday":
		console.log("You need to wear Green");
		break;
	case "friday":
		console.log("You need to wear Blue");
		break;
	case "saturday":
		console.log("You need to wear Indigo");
		break;
	case "sunday":
		console.log("You need to wear Violet");
		break;
	default:
		console.log("You should wear Black");	
}